const propertyMatchNames = {};

function addPropertyMatchName(matchName, name, valueType) {
  if (!matchName || typeof matchName !== "string") return;
  if (!propertyMatchNames[matchName]) {
    propertyMatchNames[matchName] = {
      name: name || matchName,
      valueType: valueType || "NO_VALUE",
    };
  }
}

function collectPropertyMatchNames(node) {
  if (!node || typeof node !== "object") return;
  if (node.matchName) addPropertyMatchName(node.matchName, node.name, node.valueType);
  for (const value of Object.values(node)) {
    if (Array.isArray(value)) {
      for (const child of value) collectPropertyMatchNames(child);
    } else if (value && typeof value === "object") {
      collectPropertyMatchNames(value);
    }
  }
}

